package com.demo.shop.response;

public class RevenueResponse {
	
	private Integer revenueByDay;
	
	private Integer revenueByWeek;
	
	private Integer revenueByMonth;
	
	private Integer revenueByYear;

	public Integer getRevenueByDay() {
		return revenueByDay;
	}

	public void setRevenueByDay(Integer revenueByDay) {
		this.revenueByDay = revenueByDay;
	}

	public Integer getRevenueByWeek() {
		return revenueByWeek;
	}

	public void setRevenueByWeek(Integer revenueByWeek) {
		this.revenueByWeek = revenueByWeek;
	}

	public Integer getRevenueByMonth() {
		return revenueByMonth;
	}

	public void setRevenueByMonth(Integer revenueByMonth) {
		this.revenueByMonth = revenueByMonth;
	}

	public Integer getRevenueByYear() {
		return revenueByYear;
	}

	public void setRevenueByYear(Integer revenueByYear) {
		this.revenueByYear = revenueByYear;
	}
	
	
}
